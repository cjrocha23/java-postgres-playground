package com.example;

public class AppOperadores {
    public static void main(String[] args) {

        System.out.println();
        System.out.println("### Operadores JAVA. ###");
        System.out.println();
        // "+", "-", "/", "*", "%", "++", "--"

        double x = 9.4;
        double y = 2.3;
        double divisao;
        double resto;
        divisao = x / y;
        resto = x % y;
        System.out.println("Operação.: ("+ x +" / "+ y +")");
        System.out.println("Resultado: " + Double.toString(divisao));
        System.out.println("Resto....: " + Double.toString(resto));
        System.out.println();

        System.out.println();
        System.out.println("### Operadores de Atribuições Compostas JAVA. ###");
        System.out.println("### Operadores de Atribuições Compostas: '+=', '-=', '*=', '/=', '%='");
        System.out.println();
        
        System.out.println();
        System.out.println("### Operadores Relacionais JAVA. ###");
        System.out.println("Operadores Relacionais: '+=', '-=', '*=', '/=', '%='");
        System.out.println();

    }
}
